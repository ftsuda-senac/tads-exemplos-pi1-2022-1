package br.senac.tads.pi1b.exemplos;

public class Exemplos05 {

    public static void main(String[] args) {
        float v1 = 3.197f, v2 = 5.87f; // f no final indica que o valor é float e não double

        System.out.println("Soma: " + v1 + "+" + v2 + "=" + (v1+v2));
        System.out.println("Subtração: " + v1 + "-" + v2 + "=" + (v1-v2));
        System.out.println("Multiplicação: " + v1 + "*" + v2 + "=" + (v1*v2));
        System.out.println("Divisão: " + v1 + "/" + v2 + "=" + (v1/v2));
    }

}
